package com.ffssabcloud.module.user_module.domain;

public class Role {
	private int id;
	private Short rid;
	private String name;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public Short getRid() {
		return rid;
	}
	public void setRid(Short rid) {
		this.rid = rid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
